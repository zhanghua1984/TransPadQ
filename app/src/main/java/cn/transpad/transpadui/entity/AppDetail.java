package cn.transpad.transpadui.entity;

import java.util.ArrayList;

/**
 * Created by user on 2015/6/17.
 */
public class AppDetail {
    private long id;
    private String imageUrl;
    private String name;
    private String type;
    private String size;
    private String version;
    private String downloadAmount;
    private String recommend;
    private String description;
    private ArrayList<String> imageList;

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getImageUrl() {
        return imageUrl;
    }

    public void setImageUrl(String imageUrl) {
        this.imageUrl = imageUrl;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public String getDownloadAmount() {
        return downloadAmount;
    }

    public void setDownloadAmount(String downloadAmount) {
        this.downloadAmount = downloadAmount;
    }

    public String getRecommend() {
        return recommend;
    }

    public void setRecommend(String recommend) {
        this.recommend = recommend;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public ArrayList<String> getImageList() {
        return imageList;
    }

    public void setImageList(ArrayList<String> imageList) {
        this.imageList = imageList;
    }
}
